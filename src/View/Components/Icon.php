	/**
	 * Resolve the icon name to inline SVG markup.
	 *
	 * @since 1.0.0
	 *
	 * @param string $name  Icon name.
	 * @param string $class CSS classes.
	 *
	 * @return string SVG markup.
	 */
	protected function resolveIcon( string $name, string $class ): string
	{
		$map = self::getSvgMap();

		// Try exact name first, then without common prefixes (o-, s-, fa-).
		$key = $map[ $name ] ?? null;

		if ( null === $key ) {
			$stripped = preg_replace( '/^(o-|s-|fa-)/', '', $name ) ?? $name;
			$key      = $map[ $stripped ] ?? null;
		}

		if ( null !== $key ) {
			// Inject the class attribute into the SVG tag.
			return str_replace( '<svg ', '<svg class="' . e( $class ) . '" ', $key );
		}

		// Fallback: cube icon.
		return '<svg class="' . e( $class ) . '" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5" aria-hidden="true"><path stroke-linecap="round" stroke-linejoin="round" d="m21 7.5-9-5.25L3 7.5m18 0-9 5.25m9-5.25v9l-9 5.25M3 7.5l9 5.25M3 7.5v9l9 5.25m0-9v9" /></svg>';
	}
